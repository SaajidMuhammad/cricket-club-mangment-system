    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        int x =  JOptionPane.showConfirmDialog(null, "Do You Really Want to Change Club Name");

        if (x==0)
        {

            String cid = jTextField_cid1.getText();
            String cname = jTextField_club.getText();
            String owner = jTextField_owner.getText();
            String regid = jTextField_regid.getText();
            
         

            if(cname.equals(""))
            {
                JOptionPane.showMessageDialog(null,"Please Enter Your Club Name");
            }
        
            
            else
            {

                String sql = "UPDATE club_info SET Club_ID = '"+ cid +"',Club_Name = '"+ cname +"',Club_Owner = '"+ owner +"',Reg_Id = '"+ regid +"' WHERE Club_ID = '"+ cid +"'";

                try
                {
                    pst = con.prepareStatement(sql);
                    pst.execute();

                    JOptionPane.showMessageDialog(null,"Your Club Info was Changed");

                }
                catch (Exception e)
                {
                    JOptionPane.showMessageDialog(null, e.toString(), "InfoBox", JOptionPane.INFORMATION_MESSAGE);

                }

            }
        }
        
        ClubName();

       

    }//GEN-LAST:event_jButton2ActionPerformed

