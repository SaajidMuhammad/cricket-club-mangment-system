     public void ClubName()
    {
        try{
            String q = "SELECT * FROM club_info ";
            
            pst = con.prepareStatement(q);
            rs = pst.executeQuery();
            
            while(rs.next())
            {
                String cname = rs.getString("Club_Name");
                String owner = rs.getString("Club_Owner");
                String regid = rs.getString("Reg_Id");
                
                
                jTextField_club.setText(cname);
                jTextField_owner.setText(owner);
                jTextField_regid.setText(regid);
                
            
            
            }
            
        
        
        
        }
        
        catch(Exception e)
        {
            System.out.println(e);
        }
    
    
    
    
    
    
    
    
    
    }
